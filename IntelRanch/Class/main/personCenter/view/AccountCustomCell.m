#import "AccountCustomCell.h"

@implementation AccountCustomCell

-(instancetype)initWithStyle:(UITableViewCellStyle)style reuseIdentifier:(NSString *)reuseIdentifier
{
    if (self = [super initWithStyle:style reuseIdentifier:reuseIdentifier]) {
        
        self = [[NSBundle mainBundle] loadNibNamed:[NSString stringWithString:NSStringFromClass([self class])] owner:self options:nil].firstObject;
        self.selectionStyle = UITableViewCellSelectionStyleNone;
        
        
    }
    return self;
}
- (IBAction)callPhone:(id)sender {
    
    [[UIApplication sharedApplication] openURL:[NSURL URLWithString:self.mobile]];
}

